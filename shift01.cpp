#include <iostream>
using namespace std;

int main()
{
  short a = 12, b = 100, c = -50;
  
  cout << "00001100 << 1 = " << (a << 1) << endl; // 00011000
  cout << "00001100 << 2 = " << (a << 2) << endl; // 00110000
  cout << "00001100 << 3 = " << (a << 3) << endl; // 01100000
  cout << "00001100 << 4 = " << (a << 4) << endl; // 11000000
  
  cout << "01100100 >> 1 = " << (b >> 1) << endl; // 00110010
  cout << "01100100 >> 2 = " << (b >> 2) << endl; // 00011001
  cout << "01100100 >> 3 = " << (b >> 3) << endl; // 00001100
  cout << "01100100 >> 4 = " << (b >> 4) << endl; // 00000110
  
  cout << "11001110 << 1 = " << (c << 1) << endl; // 10011100
  cout << "11001110 >> 1 = " << (c >> 1) << endl; // 11100111
  
  return 0;
}
